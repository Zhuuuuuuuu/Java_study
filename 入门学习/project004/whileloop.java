package learn.project004;

public class whileloop {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println("我错了" + i);
        }

        System.out.println("=====================");
        int i = 1;
        while (i <= 10){
            System.out.println("错什么错"+i);
            i++ ;

        }
    }
}
